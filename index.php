        var mainSlider;

        $(document).ready(function() {

            mainSlider = $('.owl-carousel');

            mainSlider.owlCarousel({
                nav: true,
                autoplay: false,
                autoplayTimeout: 5000,
                lazyLoad: true,
                loop: false,
                items: 1,
                smartSpeed: 3000,
                dots: false,
            });

            mainSlider.on('changed.owl.carousel', function(property) {
                var current = property.item.index;
                var prev = $(property.target).find(".owl-item").eq(current).prev().find("img", "hide").attr('data-navipicture');
                var next = $(property.target).find(".owl-item").eq(current).next().find("img", "hide").attr('data-navipicture');

                $('.navPrev').find('img', '.hide').attr('src', prev);
                $('.navNext').find('img', '.hide').attr('src', next);
            });

        });

        $('.navNext').on('click', function() {
            mainSlider.trigger('next.owl.carousel', [300]);
            return false;
        });

        $('.navPrev').on('click', function() {
            mainSlider.trigger('prev.owl.carousel', [300]);
            return false;
        });
        new WOW().init();
        $(document).ready(function() {
            $(".hamburger").click(function() {
                $(this).toggleClass("is-active");
            });
        });

